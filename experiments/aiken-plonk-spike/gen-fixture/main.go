// Command gen-fixture reads the exported artifacts + golden vectors and emits
// the Aiken constants module (aiken/lib/plonk_spike/fixtures.ak) for the
// hand-written PLONK verifier spike. VK points are stored compressed in the
// artifact; this tool reconstructs them to also emit the uncompressed bytes the
// Fiat-Shamir transcript hashes. THROWAWAY spike tooling.
package main

import (
	"encoding/hex"
	"encoding/json"
	"fmt"
	"os"
	"strings"

	bls12381 "github.com/consensys/gnark-crypto/ecc/bls12-381"
)

func main() {
	dir := "./artifacts"
	out := "./aiken/lib/plonk_spike/fixtures.ak"
	if len(os.Args) > 1 {
		dir = os.Args[1]
	}
	if len(os.Args) > 2 {
		out = os.Args[2]
	}
	if err := run(dir, out); err != nil {
		fmt.Fprintln(os.Stderr, "FAIL:", err)
		os.Exit(1)
	}
}

func run(dir, out string) error {
	var vk map[string]any
	var pf map[string]any
	var gd map[string]any
	if err := readJSON(dir+"/outer_vk.json", &vk); err != nil {
		return err
	}
	if err := readJSON(dir+"/outer_proof.json", &pf); err != nil {
		return err
	}
	if err := readJSON(dir+"/golden.json", &gd); err != nil {
		return err
	}

	var b strings.Builder
	b.WriteString("// GENERATED by gen-fixture — do not edit. PLONK spike fixture constants.\n")
	b.WriteString("// Source: experiments/aiken-plonk-spike/artifacts/{outer_vk,outer_proof,golden}.json\n\n")

	// scalars + ints
	ci := vk["commitment_constraint_indexes"].([]any)
	emitInt(&b, "vk_size", int(vk["size"].(float64)))
	emitInt(&b, "vk_nb_public", int(vk["nb_public_variables"].(float64)))
	emitInt(&b, "vk_commitment_index", int(ci[0].(float64)))
	emitBA(&b, "vk_size_inv", vk["size_inv"].(string))
	emitBA(&b, "vk_generator", vk["generator"].(string))
	emitBA(&b, "vk_coset_shift", vk["coset_shift"].(string))

	kzg := vk["kzg"].(map[string]any)
	emitBA(&b, "kzg_g1", kzg["g1"].(string))
	emitBA(&b, "kzg_g2_0", kzg["g2_0"].(string))
	emitBA(&b, "kzg_g2_1", kzg["g2_1"].(string))

	// VK G1 points: emit compressed (_c) + reconstructed uncompressed (_u).
	s := vk["s"].([]any)
	emitG1FromCompressed(&b, "s0", s[0].(string))
	emitG1FromCompressed(&b, "s1", s[1].(string))
	emitG1FromCompressed(&b, "s2", s[2].(string))
	for _, n := range []string{"ql", "qr", "qm", "qo", "qk"} {
		emitG1FromCompressed(&b, n, vk[n].(string))
	}
	qcp := vk["qcp"].([]any)
	emitG1FromCompressed(&b, "qcp0", qcp[0].(string))

	// public inputs (32B BE each)
	emitBAList(&b, "public_inputs", strSlice(pf["public_inputs"].([]any)))

	// proof points (already carry c + u)
	lro := pf["lro"].([]any)
	emitG1Obj(&b, "lro0", lro[0].(map[string]any))
	emitG1Obj(&b, "lro1", lro[1].(map[string]any))
	emitG1Obj(&b, "lro2", lro[2].(map[string]any))
	emitG1Obj(&b, "z", pf["z"].(map[string]any))
	h := pf["h"].([]any)
	emitG1Obj(&b, "h0", h[0].(map[string]any))
	emitG1Obj(&b, "h1", h[1].(map[string]any))
	emitG1Obj(&b, "h2", h[2].(map[string]any))
	bsb := pf["bsb22_commitments"].([]any)
	emitG1Obj(&b, "bsb0", bsb[0].(map[string]any))

	batched := pf["batched_proof"].(map[string]any)
	emitG1Obj(&b, "batched_h", batched["h"].(map[string]any))
	emitBAList(&b, "claimed_values", strSlice(batched["claimed_values"].([]any)))
	zshift := pf["z_shifted_opening"].(map[string]any)
	emitG1Obj(&b, "zshift_h", zshift["h"].(map[string]any))
	emitBA(&b, "zshift_val", zshift["claimed_value"].(string))

	// computed linearized-poly digest (baked; bound on-chain via compress check)
	emitBA(&b, "lin_digest_c", gd["lin_digest_compressed"].(string))
	emitBA(&b, "lin_digest_u", gd["lin_digest_uncompressed"].(string))

	// golden challenge values for stage-by-stage inline tests
	b.WriteString("\n// --- golden vectors (refverify) ---\n")
	for _, k := range []string{"gamma", "beta", "alpha", "zeta", "pi", "gamma_kzg", "batch_lambda_u", "folded_digest_compressed", "folded_eval"} {
		emitBA(&b, "golden_"+k, gd[k].(string))
	}
	hc := gd["hashed_commitments"].([]any)
	emitBA(&b, "golden_hashed_cmt0", hc[0].(string))

	if err := os.WriteFile(out, []byte(b.String()), 0o644); err != nil {
		return err
	}
	fmt.Fprintf(os.Stderr, "wrote %s\n", out)
	return nil
}

func emitInt(b *strings.Builder, name string, v int) {
	fmt.Fprintf(b, "pub const %s: Int = %d\n", name, v)
}
func emitBA(b *strings.Builder, name, hexstr string) {
	fmt.Fprintf(b, "pub const %s: ByteArray = #\"%s\"\n", name, hexstr)
}
func emitBAList(b *strings.Builder, name string, items []string) {
	fmt.Fprintf(b, "pub const %s: List<ByteArray> = [\n", name)
	for _, it := range items {
		fmt.Fprintf(b, "  #\"%s\",\n", it)
	}
	b.WriteString("]\n")
}

// emitG1Obj emits _c (compressed) and _u (uncompressed) from a {"c","u"} object.
func emitG1Obj(b *strings.Builder, name string, o map[string]any) {
	emitBA(b, name+"_c", o["c"].(string))
	emitBA(b, name+"_u", o["u"].(string))
}

// emitG1FromCompressed reconstructs the point to also emit its uncompressed form.
func emitG1FromCompressed(b *strings.Builder, name, compressed string) {
	raw, err := hex.DecodeString(compressed)
	if err != nil {
		panic(err)
	}
	var p bls12381.G1Affine
	if _, err := p.SetBytes(raw); err != nil {
		panic(fmt.Sprintf("%s: %v", name, err))
	}
	u := p.RawBytes()
	emitBA(b, name+"_c", compressed)
	emitBA(b, name+"_u", hex.EncodeToString(u[:]))
}

func strSlice(a []any) []string {
	out := make([]string, len(a))
	for i := range a {
		out[i] = a[i].(string)
	}
	return out
}

func readJSON(path string, v any) error {
	b, err := os.ReadFile(path)
	if err != nil {
		return err
	}
	return json.Unmarshal(b, v)
}
